# Standard 2D grid 8-neighbor graph
# ============================================================
def build_grid_8_neighbor_graph(height, width):
    """
    Build a standard 2D grid 8-neighbor graph.
    """
    print("Building the standard 2D grid 8-neighbor graph...")
    # Global node indices: (height, width)
    node_ids = torch.arange(
        height * width,
        dtype=torch.long
    ).reshape(height, width)
    # Number of undirected node pairs in the standard 8-neighbor graph:
    # horizontal + vertical + two diagonal directions
    num_undirected_pairs = (
        height * (width - 1)
        + (height - 1) * width
        + 2 * (height - 1) * (width - 1)
    )
    # Store both directions for each undirected node pair
    edge_index = torch.empty(
        (2, 2 * num_undirected_pairs),
        dtype=torch.long
    )
    cursor = 0
    def add_bidirectional_edges(source_nodes, target_nodes):
        nonlocal cursor
        source_nodes = source_nodes.reshape(-1)
        target_nodes = target_nodes.reshape(-1)
        num_edges = source_nodes.numel()
        # Forward edges: source -> target
        edge_index[0, cursor:cursor + num_edges] = source_nodes
        edge_index[1, cursor:cursor + num_edges] = target_nodes
        cursor += num_edges
        # Reverse edges: target -> source
        edge_index[0, cursor:cursor + num_edges] = target_nodes
        edge_index[1, cursor:cursor + num_edges] = source_nodes
        cursor += num_edges
    # 1. Horizontal adjacency: left <-> right
    add_bidirectional_edges(
        node_ids[:, :-1],
        node_ids[:, 1:]
    )
    # 2. Vertical adjacency: top <-> bottom
    add_bidirectional_edges(
        node_ids[:-1, :],
        node_ids[1:, :]
    )
    # 3. Main diagonal adjacency: upper-left <-> lower-right
    add_bidirectional_edges(
        node_ids[:-1, :-1],
        node_ids[1:, 1:]
    )
    # 4. Secondary diagonal adjacency: upper-right <-> lower-left
    add_bidirectional_edges(
        node_ids[:-1, 1:],
        node_ids[1:, :-1]
    )
    print(
        f"Standard 8-neighbor graph construction completed: "
        f"number of nodes={height * width}, "
        f"number of directed edges={edge_index.shape[1]}"
    )
    return edge_index
