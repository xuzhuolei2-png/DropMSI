# Moran's I related functions
# ============================================================
def build_tissue_neighbor_edges(tissue_mask, connectivity=8):
    """
    Build 2D spatial adjacency edges only within the tissue region.
    """
    height, width = tissue_mask.shape
    id_map = -np.ones(
        (height, width),
        dtype=np.int64
    )
    id_map[tissue_mask] = np.arange(np.sum(tissue_mask))
    if connectivity == 4:
        offsets = [
            (1, 0),
            (0, 1)
        ]
    elif connectivity == 8:
        offsets = [
            (1, 0),
            (0, 1),
            (1, 1),
            (1, -1)
        ]
    else:
        raise ValueError("connectivity must be 4 or 8")
    src_list = []
    dst_list = []
    for dr, dc in offsets:
        r0 = max(0, -dr)
        r1 = min(height, height - dr)
        c0 = max(0, -dc)
        c1 = min(width, width - dc)
        source_block = id_map[r0:r1, c0:c1]
        target_block = id_map[
            r0 + dr:r1 + dr,
            c0 + dc:c1 + dc
        ]
        valid_mask = (
            (source_block >= 0)
            & (target_block >= 0)
        )
        source_nodes = source_block[valid_mask].ravel()
        target_nodes = target_block[valid_mask].ravel()
        src_list.append(source_nodes)
        dst_list.append(target_nodes)
        src_list.append(target_nodes)
        dst_list.append(source_nodes)
    if len(src_list) > 0:
        src = np.concatenate(src_list)
        dst = np.concatenate(dst_list)
    else:
        src = np.array([], dtype=np.int64)
        dst = np.array([], dtype=np.int64)
    return src, dst
def moran_i_binary(x, src, dst):
    """
    Compute Global Moran's I.
    """
    x = x.astype(np.float64)
    num_nodes = x.size
    if num_nodes == 0 or len(src) == 0:
        return np.nan
    x_mean = x.mean()
    centered_x = x - x_mean
    denominator = np.sum(centered_x ** 2)
    if denominator < 1e-12:
        return np.nan
    total_weight = len(src)
    numerator = np.sum(centered_x[src] * centered_x[dst])
    return (num_nodes / total_weight) * (numerator / denominator)
def compute_cluster_moran_metrics(
    spatial_cluster_map,
    tissue_mask,
    n_clusters,
    connectivity=8
):
    """
    Compute Moran's I for each cluster region.
    """
    src, dst = build_tissue_neighbor_edges(
        tissue_mask,
        connectivity
    )
    tissue_cluster_labels = spatial_cluster_map[tissue_mask]
    records = []
    for cluster_id in range(n_clusters):
        cluster_binary = (
            tissue_cluster_labels == cluster_id
        ).astype(np.float64)
        area = int(np.sum(cluster_binary))
        moran_value = (
            moran_i_binary(cluster_binary, src, dst)
            if area > 0
            else np.nan
        )
        records.append(
            {
                "cluster": cluster_id,
                "area": area,
                "moran_I": moran_value
            }
        )
    return pd.DataFrame(records)
