    class ESULoss(nn.Module):
        def __init__(self, eps=1e-10):
            super().__init__()
            self.eps = eps
        def forward(self, X, mean_pred, s, M):
            batch_size, num_nodes, dim = X.shape
            X_flat = X.view(-1, dim)
            mean_flat = mean_pred.view(-1, dim)
            s_flat = s.view(-1, dim)
            M_flat = M.view(-1, dim)
            decay_term = (
                torch.exp(-s_flat)
                * torch.abs(X_flat - mean_flat)
            )
            sparse_term = 2 * s_flat
            masked_decay = decay_term * M_flat
            masked_sparse = sparse_term * M_flat
            total_elem = M_flat.sum() + self.eps
            esu_loss = (
                torch.sum(masked_decay)
                + torch.sum(masked_sparse)
            ) / total_elem
            return esu_loss
