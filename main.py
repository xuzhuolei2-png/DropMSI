# Map tissue-region cluster labels back to the 2D spatial grid
# ============================================================
def map_to_spatial_grid(
    cluster_labels,
    tissue_pixel_indices,
    height,
    width
):
    """
    Map cluster labels from the tissue region to a 2D spatial matrix.
    """
    spatial_cluster_map = np.full(
        (height, width),
        -1,
        dtype=int
    )
    for local_idx, global_idx in enumerate(tissue_pixel_indices):
        row_idx, col_idx = divmod(global_idx, width)
        spatial_cluster_map[row_idx, col_idx] = cluster_labels[local_idx]
    return spatial_cluster_map
